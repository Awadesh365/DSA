//v flip the char:- 'a' to 'z' , 'b' to 'y' , 'c' to 'x'  and so on.
// in small letters
char flip_char(char C)
{
    if (C == 'a')
        return 'z';
    else if (C == 'z')
        return 'a';
    else if (C == 'b')
        return 'y';
    else if (C == 'y')
        return 'b';
    else if (C == 'c')
        return 'x';
    else if (C == 'x')
        return 'c';
    else if (C == 'd')
        return 'w';
    else if (C == 'w')
        return 'd';
    else if (C == 'e')
        return 'v';
    else if (C == 'v')
        return 'e';
    else if (C == 'f')
        return 'u';
    else if (C == 'u')
        return 'f';
    else if (C == 'g')
        return 't';
    else if (C == 't')
        return 'g';
    else if (C == 'h')
        return 's';
    else if (C == 's')
        return 'h';
    else if (C == 'i')
        return 'r';
    else if (C == 'r')
        return 'i';
    else if (C == 'j')
        return 'q';
    else if (C == 'q')
        return 'j';
    else if (C == 'k')
        return 'p';
    else if (C == 'p')
        return 'k';
    else if (C == 'l')
        return 'o';
    else if (C == 'o')
        return 'l';
    else if (C == 'm')
        return 'n';
    else if (C == 'n')
        return 'm';
}

