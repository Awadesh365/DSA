// Function to return trailing
// 0s in factorial of n

ll Find_Lastzeros(ll n)
{
    if (n < 0)
        return -1;

    int cnt = 0;
    for (ll i = 5; (n / i) >= 1; i *= 5)
        cnt += (n / i);

    return cnt;
}
