const ll N = 1e6;
void solve()
{
    int n, m;
    cin >> n >> m;

    vector<int> arr1(n);
    vector<int> arr2(m);
    vector<int> sol(n + m);

    for (auto &it : arr1)
        cin >> it;

    for (auto &it : arr2)
        cin >> it;

    int k = 0, i = 0, j = 0;
    while (i < n || j < m)
    {
        if (j == m || i < n && arr1[i] < arr2[j])
        {
            sol[k] = arr1[i];
            i++, k++;
        }
        else
        {
            sol[k] = arr2[j];
            j++, k++;
        }
    }

    for (auto &it : sol)
        cout << it << " ";

    cout << endl;
}
