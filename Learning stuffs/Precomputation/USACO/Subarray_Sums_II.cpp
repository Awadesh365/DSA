void solve()
{
    ll n, x;
    cin >> n >> x;
    vector<ll> a(n);
    for (auto &it : a)
        cin >> it;

    ll prefix_sum = 0;
    ll ans = 0;
    map<ll, ll> sums;
    sums[0] = 1;

    for (int it : a)
    {
        prefix_sum += it;

        ans += sums[prefix_sum - x];

        sums[prefix_sum]++;
    }
    cout << ans << endl;
}
